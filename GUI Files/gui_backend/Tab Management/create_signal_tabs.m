end
function updateSTDValue(src, handles,stdval)
    h = guidata(handles.figure);
    % Callback function to update STD value
    new_val = get(src, 'Value');
    set(stdval, 'String', num2str(new_val, '%.1f'));
    update_thresholds(h);
    
end
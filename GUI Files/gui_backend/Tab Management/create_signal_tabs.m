% Add std_textbox within the tab2 panel (h.tab2)
h.std_textbox = uicontrol( ...
    'Parent', h.figure, ...
    'Style', 'text', ...
    'Units', 'normalized', ...
    'Position', [0.23 0.05 0.15 0.04], ...
    'HorizontalAlignment', 'Left', ...
    'FontName', 'Calibri', ...
    'BackgroundColor', backgdcolor, ...
    'ForegroundColor',accentcolor,'Visible', 'off','String', 'Minimum Spike Threshold Multiplier');
h.std_value = uicontrol(...

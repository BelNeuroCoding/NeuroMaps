    'Style','edit','Units','normalized','Position',[0.37 0.005 0.07 0.04], ...
    'String','0,60','Callback', @(src,evt) pop_graph_callback(h,src));
h.waterfall_settings_button = uicontrol('Parent', h.waterfall_tab, ...
'Style','pushbutton',...
'Units','normalized',...
'Position',[0.6 0.005 0.15 0.04],...  % adjust location
'String','Plot Settings',...
'BackgroundColor',backgdcolor,...
'ForegroundColor',accentcolor,...
'Callback', @(src,evt) waterfall_plot_settings(h));

h.export_waterfall_button = uicontrol('Parent', h.waterfall_tab, ...
'Style','pushbutton',...
'Units','normalized',...
'Position',[0.85 0.005 0.09 0.04],...  % adjust location
'String','Export',...
'BackgroundColor',backgdcolor,...
'ForegroundColor',accentcolor,...
'Callback', @(src,evt) export_full_waterfall_page(h));
h.plot_waterfall_button = uicontrol('Parent', h.waterfall_tab, ...
'Style','pushbutton',...
'Units','normalized',...
'Position',[0.75 0.005 0.09 0.04],...  % adjust location
'String','Plot',...
'BackgroundColor',backgdcolor,...
'ForegroundColor',accentcolor,...
'Callback', @(src,evt) pop_graph_callback(h));



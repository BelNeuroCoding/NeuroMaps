function openFilterSelectionDialog(h)
    h = guidata(h.figure);
    % Dialog size
    dialogHeight = 300;  
    d = dialog('Position', [400, 300, 450, dialogHeight], ...
               'Name', 'Configure Filters', ...
               'Color', backgdcolor);

    % Filter options (with parameter labels)
    filters = { ...
        'AC Line Noise', {'Notch Freq (Hz):', '50';'Bandwidth:', '2'}; ...
        'Low-pass Filter (LFP)', {'Cutoff (Hz):', '300'; 'Downsampled Frequency:', '1000'}; ...
        'Bandpass Filter (Spikes)', {'Low (Hz):', '300'; 'High (Hz):', '3000'; 'Order:', '3'} ...
    };

    numFilters = size(filters,1);
    cbHandles = gobjects(1,numFilters);
    paramHandles = cell(1,numFilters);

    % Layout each filter row
    for i = 1:numFilters
        yBase = 0.9 - (i-1)*0.25;

        % Checkbox
        cbHandles(i) = uicontrol('Parent', d, ...
            'Style', 'checkbox', ...
            'Units', 'normalized', ...
            'Position', [0.05, yBase-0.1, 0.4, 0.15], ...
            'String', {filters{i,1}}, ...
            'FontSize', 10, ...
            'BackgroundColor', backgdcolor, ...
            'ForegroundColor', accentcolor);
            % Parameters for this filter
        params = filters{i,2};
        paramEdits = gobjects(size(params,1),1);
        for j = 1:size(params,1)
            % Label
            uicontrol('Parent', d, ...
                'Style', 'text', ...
                'Units', 'normalized', ...
                'Position', [0.5, yBase - 0.07*(j-1), 0.2, 0.05], ...
                'String', params{j,1}, ...
                'BackgroundColor', backgdcolor, ...
                'HorizontalAlignment', 'right', ...
                'ForegroundColor', accentcolor);

            % Edit box
            paramEdits(j) = uicontrol('Parent', d, ...
                'Style', 'edit', ...
                'Units', 'normalized', ...
                'Position', [0.7, yBase - 0.07*(j-1), 0.2, 0.05], ...
                'String', params{j,2}, ...
                'BackgroundColor', 'w');
        end
        paramHandles{i} = paramEdits;
    end

    % Confirm button
    uicontrol('Parent', d, ...
        'Style', 'pushbutton', ...
        'Position', [120, 10, 100, 30], ...
        'String', 'Apply Filters', ...
        'Callback', @(src,event) confirmFilterSelection(h,d,cbHandles,paramHandles,filters));


function confirmFilterSelection(h,d,cbHandles,paramHandles,filters)
    selectedFilters = struct();

    for i = 1:length(cbHandles)
        if get(cbHandles(i), 'Value') == 1
            params = filters{i,2};
            paramEdits = paramHandles{i};
            vals = cell(size(params,1),1);
            for j = 1:size(params,1)
                vals{j} = str2double(get(paramEdits(j), 'String'));
            end
            % Build struct entry
            filterName = matlab.lang.makeValidName(filters{i,1});
            paramName = matlab.lang.makeValidName(params(:,1));
            selectedFilters.(filterName) = cell2struct(vals, paramName, 1);
        end
    end

    if isempty(fieldnames(selectedFilters))
        msgbox('Please select at least one filter.', 'Error', 'error');
    else
        h.selectedFilters = selectedFilters;
        delete(d);
        % Call your processing function with filters + params
        applyFilters(h);
    end

end
end

function stepperSet(h)
% Visually highlight the active step, dim the others
    idx = unique(h.stepCurrent);
    for i = 1:numel(h.stepBtns)
        if ismember(i,idx)
            set(h.stepBtns(i), 'BackgroundColor', hAccent(), 'ForegroundColor',[1 1 1], ...
                'FontWeight','bold');
        else
            set(h.stepBtns(i), 'BackgroundColor', [0.94 0.94 0.94], ...
                'ForegroundColor', [0.25 0.25 0.25], 'FontWeight','normal');
        end
    end
    guidata(h.figure,h);
end



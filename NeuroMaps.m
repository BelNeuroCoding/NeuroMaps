function stepperGo(idx)
    h=guidata(h.figure);
    switch idx
        case 1   % Load
                try openSystemSelectionDialog(h); catch, end
            % Focus a relevant tab
            try h.tabgroup1.SelectedTab = h.overview_main_tab; catch, end
        case 2   % Filter
            try openFilterSelectionDialog(h); catch, end
        case 3   % Reference
            try run_referencing_callback([],[],h); catch, end
        case 4   % Detect Spikes
            try run_spike_analysis(h); catch, end
            h=guidata(h.figure);
            try h.tabgroup1.SelectedTab = h.spike_detection_tab; catch, end
            try h.tabgroup2.SelectedTab = h.spikes_main_tab; catch, end
        case 5   %Validate Clusters
            try validate_clusters_callback(h); catch,end
        case 6   % LFP Analysis
            try fooof_callback(h); catch, end
    end
    h=guidata(h.figure);
    % Track state
    h.stepCurrent = [h.stepCurrent, idx];          % current step index
    % Initial highlight
    stepperSet(h);
    guidata(h.figure,h)
end

